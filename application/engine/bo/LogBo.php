class LogBo {
	var $pdo = null;

	var $TABLE = "logs";
	var $ID_FIELD = "log_id";

	function __construct($pdo, $config = null) {

		$this->pdo = $pdo;
	}

	static function newInstance($pdo, $config = null) {
		return new LogBo($pdo, $config);
	}

	function create(&$log) {
		$query = "	INSERT INTO $this->TABLE () VALUES ()	";

		$statement = $this->pdo->prepare($query);
//				echo showQuery($query, $args);

		try {
			$statement->execute();
			$log[$this->ID_FIELD] = $this->pdo->lastInsertId();

			return true;
		}
		catch(Exception $e){
			echo 'Erreur de requÃ¨te : ', $e->getMessage();
		}

		return false;
	}

	function update($log) {
		$query = "	UPDATE $this->TABLE SET ";

		$separator = "";
		foreach($log as $field => $value) {
			$query .= $separator;
			$query .= $field . " = :". $field;
			$separator = ", ";
		}

		$query .= "	WHERE $this->ID_FIELD = :$this->ID_FIELD ";

//		echo showQuery($query, $log);

		$statement = $this->pdo->prepare($query);
		$statement->execute($log);
	}

	function save(&$log) {
 		if (!isset($log[$this->ID_FIELD]) || !$log[$this->ID_FIELD]) {
			$this->create($log);
		}

		$this->update($log);
	}

	function getById($id) {
		$filters = array($this->ID_FIELD => intval($id));

		$results = $this->getByFilters($filters);

		if (count($results)) {
			return $results[0];
		}

		return null;
	}

	function getByFilters($filters = null) {
		if (!$filters) $filters = array();
		$args = array();

		$query = "	SELECT *
					FROM  $this->TABLE
					WHERE
						1 = 1 \n";

		if (isset($filters[$this->ID_FIELD])) {
			$args[$this->ID_FIELD] = $filters[$this->ID_FIELD];
			$query .= " AND $this->ID_FIELD = :$this->ID_FIELD \n";
		}

//		$query .= "	ORDER BY gro_label, the_label ";

		$statement = $this->pdo->prepare($query);
//		echo showQuery($query, $args);

		$results = array();

		try {
			$statement->execute($args);
			$results = $statement->fetchAll();

			foreach($results as $index => $line) {
				foreach($line as $field => $value) {
					if (is_numeric($field)) {
						unset($results[$index][$field]);
					}
				}
			}
		}
		catch(Exception $e){
			echo 'Erreur de requÃ¨te : ', $e->getMessage();
		}

		return $results;
	}
}